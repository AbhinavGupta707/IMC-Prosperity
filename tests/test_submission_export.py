@pytest.mark.unit
def test_verify_live_module_order_detects_missing_module(
    tmp_path: Path,
) -> None:
    """Simulate a live module that imports something not in the order list.

    We build a tiny fake repo under ``tmp_path`` with two modules:
    ``src/core/entry.py`` imports ``src.core.dep``, but we only pass
    ``entry.py`` to the verifier. The verifier must raise a
    RuntimeError that mentions the missing target.
    """
    fake_root = tmp_path / "fake_repo"
    (fake_root / "src" / "core").mkdir(parents=True)
    (fake_root / "src" / "__init__.py").write_text("")
    (fake_root / "src" / "core" / "__init__.py").write_text("")
    (fake_root / "src" / "datamodel.py").write_text("class TradingState: ...\n")
    (fake_root / "src" / "core" / "entry.py").write_text(
        "from src.core.dep import helper\n" "def run():\n" "    return helper()\n"
    )
    (fake_root / "src" / "core" / "dep.py").write_text("def helper():\n    return 1\n")

    with pytest.raises(RuntimeError) as excinfo:
        verify_live_module_order(["src/core/entry.py"], root=fake_root)

    message = str(excinfo.value)
    assert "src.core.dep" in message
    assert "LIVE_MODULE_ORDER" in message


