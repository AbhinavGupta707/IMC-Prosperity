@pytest.mark.unit
def test_validator_soft_size_emits_warning() -> None:
    assert SOFT_SIZE_BYTES < MAX_SIZE_BYTES
    # Craft a bundle just over the soft threshold but well under the
    # hard one. We pad with a harmless comment so the file still
    # parses and still has a valid Trader class.
    pad_bytes = SOFT_SIZE_BYTES + 500
    padding = "# " + ("x" * (pad_bytes - 2)) + "\n"
    source = padding + "class Trader:\n" + "    def run(self, s):\n" + "        return {}, 0, ''\n"
    assert SOFT_SIZE_BYTES < len(source.encode("utf-8")) < MAX_SIZE_BYTES

    report = validate_source(source)
    warn_codes = {issue.code for issue in report.warnings}
    assert "size_approaching_limit" in warn_codes
    # Soft breach must not fail the gate.
    assert report.ok


