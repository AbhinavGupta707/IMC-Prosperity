@pytest.mark.integration
def test_dry_run_state_round_trips_across_two_calls(tmp_path: Path) -> None:
    """Two sequential ``Trader.run()`` calls with traderData flowing.

    This proves three things about the bundled artifact:

    1. The bundled ``Trader`` instantiates.
    2. ``StateStore`` inside the bundle can parse its own output on
       the next call (schema round-trip inside the exported file).
    3. Per-product memory actually grows across iterations (so
       ``ProductMemory.recent_mids`` is really being persisted
       through ``traderData``, not just reset on every call).

    A one-shot smoke is not enough here: it would miss any bug where
    the exporter accidentally breaks the state-store parse/serialize
    cycle.
    """
    bundle = build_submission_source(ExportOptions(datamodel_mode="inline"))
    path = tmp_path / "trader_submission_roundtrip.py"
    write_submission(bundle, path)

    module_name = "_phase9_smoke_round_trip"
    try:
        module = _load_bundle_module(path, module_name)
        trader = module.Trader()

        state1 = _emeralds_state(module, timestamp=0)
        _, _, trader_data1 = trader.run(state1)
        assert isinstance(trader_data1, str) and trader_data1
        parsed1 = json.loads(trader_data1)
        assert parsed1.get("version") == 1
        mids1 = parsed1["products"]["EMERALDS"]["recent_mids"]
        assert len(mids1) == 1, "first iteration should append one mid"

        # Feed the returned traderData back in and run again.
        state2 = _emeralds_state(module, trader_data=trader_data1, timestamp=100)
        _, _, trader_data2 = trader.run(state2)
        parsed2 = json.loads(trader_data2)
        mids2 = parsed2["products"]["EMERALDS"]["recent_mids"]
        assert len(mids2) == 2, (
            "second iteration should append another mid, proving "
            "traderData round-tripped through the bundled StateStore"
        )
        # The first value must be preserved.
        assert mids2[0] == pytest.approx(mids1[0])
    finally:
        sys.modules.pop(module_name, None)


