@pytest.mark.unit
def test_strip_module_removes_future_and_src_imports() -> None:
    source = (
        '"""docstring."""\n'
        "from __future__ import annotations\n"
        "\n"
        "import math\n"
        "from src.core.types import NormalizedSnapshot\n"
        "from src.datamodel import Order\n"
        "\n"
        "def f() -> int:\n"
        "    return 1\n"
    )
    stripped = strip_module("fake/mod.py", source)

    assert "from __future__" not in stripped.body
    assert "from src." not in stripped.body
    assert "import math" not in stripped.body  # hoisted, not in body
    assert "def f() -> int" in stripped.body
    assert '"""docstring."""' in stripped.body

    assert any("import math" in imp for imp in stripped.hoisted_imports)
    assert stripped.datamodel_symbols == {"Order"}


