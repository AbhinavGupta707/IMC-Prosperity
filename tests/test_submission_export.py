def _load_bundle_module(path: Path, name: str) -> types.ModuleType:
    """Import a bundled submission file in isolation.

    The ``sys.modules`` registration before ``exec_module`` is
    required on Python >= 3.14: the stdlib ``dataclasses`` decorator
    looks up ``cls.__module__`` in ``sys.modules`` to compute type
    annotations, and raises ``AttributeError`` if the module hasn't
    been registered yet.
    """
    spec = importlib.util.spec_from_file_location(name, path)
    assert spec is not None and spec.loader is not None
    module = importlib.util.module_from_spec(spec)
    sys.modules[name] = module
    try:
        spec.loader.exec_module(module)
    except Exception:
        sys.modules.pop(name, None)
        raise
    return module


def _empty_trading_state(module: types.ModuleType) -> object:
    return module.TradingState(
        traderData="",
        timestamp=0,
        listings={},
        order_depths={},
        own_trades={},
        market_trades={},
        position={},
        observations=module.Observation(),
    )


def _emeralds_state(
    module: types.ModuleType,
    *,
    trader_data: str = "",
    timestamp: int = 0,
) -> object:
    """Build a ``TradingState`` with a minimal EMERALDS order book.

    EMERALDS is pinned around 10,000 by the default engine config, so
    a book straddling 10,000 is guaranteed to produce a mid and drive
    the market-making strategy to update ``recent_mids`` — which is
    what the round-trip smoke test asserts.
    """
    depth = module.OrderDepth(
        buy_orders={9998: 5, 9997: 10},
        sell_orders={10002: -5, 10003: -10},
    )
    return module.TradingState(
        traderData=trader_data,
        timestamp=timestamp,
        listings={},
        order_depths={"EMERALDS": depth},
        own_trades={},
        market_trades={},
        position={},
        observations=module.Observation(),
    )


@pytest.mark.integration
def test_dry_run_inline_bundle_executes(tmp_path: Path) -> None:
    bundle = build_submission_source(ExportOptions(datamodel_mode="inline"))
    path = tmp_path / "trader_submission_inline.py"
    write_submission(bundle, path)

    module_name = "_phase9_smoke_inline"
    try:
        module = _load_bundle_module(path, module_name)

        assert hasattr(module, "Trader")
        assert hasattr(module, "TradingState")

        trader = module.Trader()
        state = _empty_trading_state(module)
        result = trader.run(state)

        assert isinstance(result, tuple)
        assert len(result) == 3
        orders, conversions, trader_data = result
        assert isinstance(orders, dict)
        assert isinstance(conversions, int)
        assert isinstance(trader_data, str)
    finally:
        sys.modules.pop(module_name, None)


@pytest.mark.integration
def test_dry_run_platform_bundle_imports_with_injected_datamodel(
    tmp_path: Path,
) -> None:
    """Platform mode file loads when ``datamodel`` is injected.

    This proves the exporter emits a valid ``from datamodel import
    ...`` header and the rest of the bundle parses against the real
    Prosperity datamodel contract (which we mirror in
    ``src/datamodel.py``).
    """
    bundle = build_submission_source(ExportOptions(datamodel_mode="platform"))
    path = tmp_path / "trader_submission_platform.py"
    write_submission(bundle, path)

    # Inject our local mirror as the platform-provided ``datamodel``
    # module for the duration of the load.
    import src.datamodel as local_datamodel

    sentinel = sys.modules.get("datamodel")
    sys.modules["datamodel"] = local_datamodel
    module_name = "_phase9_smoke_platform"
    try:
        module = _load_bundle_module(path, module_name)
        trader = module.Trader()
        state = _empty_trading_state(local_datamodel)
        result = trader.run(state)
        assert len(result) == 3
    finally:
        sys.modules.pop(module_name, None)
        if sentinel is None:
            sys.modules.pop("datamodel", None)
        else:
            sys.modules["datamodel"] = sentinel


