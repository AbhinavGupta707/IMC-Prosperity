"""Delta-hedged option-native probes for the R4 VELVET voucher complex.

This harness intentionally isolates option families from the static R3
schedule:

* long-gamma probes: buy core vouchers when trailing realized vol exceeds IV;
* rich-wing probes: sell high smile-residual wings in small size;
* both families delta hedge with VELVET at the sampled book touch.

It is a mechanism test. It uses sampled IV/Greek rows generated by
``analyze_velvet_option_complex`` and should not be treated as exact live fill
simulation.
"""

from __future__ import annotations

import argparse
from dataclasses import dataclass
from pathlib import Path

import numpy as np
import pandas as pd


REPO_ROOT = Path(__file__).resolve().parents[3]
DEFAULT_FEATURES = REPO_ROOT / "outputs" / "round_4" / "velvet_option_complex" / "option_iv_greeks_smile.csv"
DEFAULT_OUT_DIR = REPO_ROOT / "outputs" / "round_4" / "gamma_smile_probes"
DEFAULT_DOC = REPO_ROOT / "docs" / "round_4" / "GAMMA_SMILE_PROBES.md"

UNDERLYING = "VELVETFRUIT_EXTRACT"
OPTION_LIMIT = 300
UNDERLYING_LIMIT = 200


@dataclass(frozen=True)
class ProbeConfig:
    label: str
    family: str
    products: tuple[str, ...]
    max_pos: int
    max_order: int
    delta_band: float
    vol_edge: float = 0.0
    residual_edge: float = 0.0
    max_spread: float = 10.0
    flatten_after: int | None = None


@dataclass
class Portfolio:
    products: tuple[str, ...]
    cash: dict[str, float]
    position: dict[str, int]
    trades: list[dict]
    peak: float = -float("inf")
    max_drawdown: float = 0.0

    @classmethod
    def create(cls, products: tuple[str, ...]) -> "Portfolio":
        all_products = (UNDERLYING, *products)
        return cls(
            products=products,
            cash={product: 0.0 for product in all_products},
            position={product: 0 for product in all_products},
            trades=[],
        )

    def buy(self, dataset: str, day: int, timestamp: int, product: str, price: float, qty: int, reason: str) -> int:
        qty = int(max(0, qty))
        if qty <= 0:
            return 0
        qty = min(qty, (UNDERLYING_LIMIT if product == UNDERLYING else OPTION_LIMIT) - self.position.get(product, 0))
        if qty <= 0:
            return 0
        self.cash[product] -= float(price) * qty
        self.position[product] = self.position.get(product, 0) + qty
        self.trades.append(_trade_row(dataset, day, timestamp, product, "buy", price, qty, reason, self.position[product]))
        return qty

    def sell(self, dataset: str, day: int, timestamp: int, product: str, price: float, qty: int, reason: str) -> int:
        qty = int(max(0, qty))
        if qty <= 0:
            return 0
        qty = min(qty, (UNDERLYING_LIMIT if product == UNDERLYING else OPTION_LIMIT) + self.position.get(product, 0))
        if qty <= 0:
            return 0
        self.cash[product] += float(price) * qty
        self.position[product] = self.position.get(product, 0) - qty
        self.trades.append(_trade_row(dataset, day, timestamp, product, "sell", price, qty, reason, self.position[product]))
        return qty

    def mark(self, mids: dict[str, float]) -> float:
        total = 0.0
        for product, cash in self.cash.items():
            total += cash + self.position.get(product, 0) * mids.get(product, 0.0)
        return total

    def observe(self, pnl: float) -> float:
        self.peak = max(self.peak, pnl)
        self.max_drawdown = min(self.max_drawdown, pnl - self.peak)
        return self.max_drawdown


def _trade_row(dataset: str, day: int, timestamp: int, product: str, side: str, price: float, qty: int, reason: str, pos_after: int) -> dict:
    return {
        "dataset": dataset,
        "day": int(day),
        "timestamp": int(timestamp),
        "product": product,
        "side": side,
        "price": float(price),
        "qty": int(qty),
        "reason": reason,
        "pos_after": int(pos_after),
    }


def load_features(path: Path) -> pd.DataFrame:
    df = pd.read_csv(path)
    # Use regular sampled snapshots for strategy evaluation, not extra fill
    # timestamps inherited from the schedule attribution pass.
    df = df[df["timestamp"].mod(1_000).eq(0)].copy()
    for column in ["iv", "rv_back_30000", "iv_residual", "price_residual", "delta"]:
        df[column] = pd.to_numeric(df[column], errors="coerce")
    return df.sort_values(["dataset", "day", "timestamp", "product"]).reset_index(drop=True)


